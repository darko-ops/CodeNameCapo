/**
 * Embed the widget HTML/JS into a compiled TS module so they survive bundling
 * (Vercel serverless can't readFileSync sibling files at runtime). Source of
 * truth stays the .html/.js files; run `npm run bundle:assets` after editing.
 */
import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const widgetDir = join(dirname(fileURLToPath(import.meta.url)), "..", "src", "widget");
const files = {
  WIDGET_HTML: "widget.html",
  SMS_HTML: "sms.html",
  EMBED_JS: "embed.js",
  DEMO_HTML: "demo.html",
  DASHBOARD_HTML: "dashboard.html",
  LANDING_HTML: "landing.html",
  ONBOARD_HTML: "onboard.html",
  RESET_HTML: "reset.html",
};

let out = `/* AUTO-GENERATED by scripts/bundle-assets.mjs — do not edit by hand.\n`;
out += `   Edit the source files in src/widget/ then run \`npm run bundle:assets\`. */\n\n`;
for (const [name, file] of Object.entries(files)) {
  const content = readFileSync(join(widgetDir, file), "utf8");
  // JSON.stringify yields a valid, fully-escaped JS string literal.
  out += `export const ${name}: string = ${JSON.stringify(content)};\n`;
}

writeFileSync(join(widgetDir, "assets.generated.ts"), out);
console.log("wrote src/widget/assets.generated.ts");

// --- brand icons -----------------------------------------------------------
// Same rationale as the HTML above: the favicon/app icons live as files in
// assets/bouncr_logo_pack/, but Vercel serverless can't read them at runtime,
// so we inline them as base64 and serve them from Hono routes (app.ts). Source
// of truth is the logo pack; re-run `npm run bundle:assets` if it changes.
const iconDir = join(dirname(fileURLToPath(import.meta.url)), "..", "assets", "bouncr_logo_pack");
const icons = {
  FAVICON_ICO_B64: "favicon.ico",
  ICON_16_B64: "bouncr_logo_16.png",
  ICON_32_B64: "bouncr_logo_32.png",
  APPLE_TOUCH_B64: "bouncr_logo_180.png",
  ICON_192_B64: "bouncr_logo_192.png",
  ICON_512_B64: "bouncr_logo_512.png",
};

let iout = `/* AUTO-GENERATED by scripts/bundle-assets.mjs — do not edit by hand.\n`;
iout += `   Brand icons (base64) from assets/bouncr_logo_pack/. Served by app.ts. */\n\n`;
for (const [name, file] of Object.entries(icons)) {
  const b64 = readFileSync(join(iconDir, file)).toString("base64");
  iout += `export const ${name}: string = ${JSON.stringify(b64)};\n`;
}

writeFileSync(join(widgetDir, "icons.generated.ts"), iout);
console.log("wrote src/widget/icons.generated.ts");
